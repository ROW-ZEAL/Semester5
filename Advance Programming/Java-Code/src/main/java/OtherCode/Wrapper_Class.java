package OtherCode;
public class Wrapper_Class {

//    Wrapper_Class - provides a way to use primitive datatype as reference datatype
//    - reference data types contain some useful methods


//    Primitive - boolean, char, int, double
//    Wrapper - Boolean, Character, Integer, Double

//    autoboxing - the automatic conversion that the Java compiler
//    makes btn the primitive types and their corresponding
//    object wrapper classes

//    unboxing - the reverse of autoboxing, Automatic conversion
//    of wrapper class to primitive

    public static void main(String[] args) {

        // Create reference datatype
        // (Wrapper class) var_name = autoboxing use to assign primitive datatype

        Boolean a = true;
        Character b = '@';
        Integer c = 123;
        Double d = 3.14;
        String e = "Elvish Yadav";

//        NOTE: Primitive datatype are faster than reference datatype

    }



}
